#!/usr/bin/python

# display is monochrome 1,4" garphic 124x64 LCD with ST7565 LCD driver
# display is placed on board with buttons
# this display is conected to raspberry pi throught SPI bus,
# buttons and LCD backlight is conected to 8-bit IO expander PCF8574 conected to
# raspberry pi throught I2C bus

# Pin conection LCD (3,3V logic) to raspberry
# LCD               RASPBERRY PI GPIO (board pin number)
#----------        --------------------
# LCD_CS               24
# LCD_RST              22
# LCD_A0               18
# LCD_CLK              23
# LCD_SI               19

# Pin 8-bit expander (5V logic) to raspberry with i2c addres 0x3c or 0x24
# PCF8574           RASPBERRY PI GPIO (board pin number)
#-----------       ---------------------
# CLK                  5 (throught 5V/3V converter)
# SDA                  3 (throught 5V/3V converter)

# Pin of 8-bit expander to  buttons and LCD backlight
# P0                   left  button
# P1                   right button
# P2                   up    button
# P3                   down  button
# P4                   ok    button
# P5                   esc   button
# P6                   LCD backlight
# P7                   not use

# great manul as use this LCD display is:
# https://m8051.blogspot.cz/2015/04/raspberry-pi-gpiospi-to-use-st7565-lcd.html
# or  https://www.raspberrypi.org/forums/viewtopic.php?f=32&t=25029
# Next example is copy from last link.

